#include <stdio.h>

int main() {
    int a = 10;
    int b = 5;
    // assignment operator 
    a += 5; // a = a + 5 
    // a = a + 5; 
    // a = 10 + 5
    // a = 15
    b -= 2; // b = b - 2;

    // *=, /= , %=

    int age = 18; 
    // age = age + 8;
    age += 5;

    printf("age = %d\n", age);


    return 0;
}