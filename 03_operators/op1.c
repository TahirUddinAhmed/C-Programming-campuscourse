#include <stdio.h>

int main() {
    int a = 12 + 8;
    int b = 6 - 3; 

    // create two variable 
    int x = 7;
    int y = 5;

    int result = x * y;

    // x = 7
    // y = 5
    // x * y = 7 * 5 = 35
    // result = 35


    // modulus operator 
    float mod = 5 % 2;

    printf("value of a %d\n", a);
    printf("value of b is %d\n", b);
    printf("value of result is %d\n", result);
    printf("value of mod is %d\n", mod);
    return 0;
}